    private void abrirConexao(){
        if (controleConexaoBanco.abrirConexao()){
            JOptionPane.showMessageDialog(this, controleConexaoBanco.getMensagem());
        }else{
            JOptionPane.showMessageDialog(this, controleConexaoBanco.getMensagem(), "Falha ao abrir conexao", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void fecharConexao(){
        if (controleConexaoBanco.fecharConexao()){
            JOptionPane.showMessageDialog(this, controleConexaoBanco.getMensagem());
        }else{
            JOptionPane.showMessageDialog(this, controleConexaoBanco.getMensagem(), "Falha ao abrir conexao", JOptionPane.ERROR_MESSAGE);
        }
    }

